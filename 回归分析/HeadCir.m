%-----------------------------------------
%  此为根据散点图的走势选取的理论回归方程
%-----------------------------------------
function    y = HeadCir1(beta, x)
y = beta(1) * exp(beta(2) ./ (x + beta(3)));

%-----------------------常用的回归方程模型-----------------
%
%	负指数函数			y=aexp(b/(x+c))
%   双曲线函数  		y=(x+a)/(b*x+c)
%   幂函数      		y=a*(x+b).^c
%   Logistic曲线函数	y=a/(1+b*exp(x+c))
%   对数函数            y=a+b*ln(x+c)
%
%-----------------------------------------------